package top.cmarco.temperatureplugin.task;

import org.jetbrains.annotations.NotNull;
import top.cmarco.temperatureplugin.manager.WorldManager;

public final class WorldSeasonUpdateRunnable implements Runnable {

    private final WorldManager worldManager;

    public WorldSeasonUpdateRunnable(@NotNull final WorldManager worldManager) {
        this.worldManager = worldManager;
    }

    /**
     * When an object implementing interface {@code Runnable} is used
     * to create a thread, starting the thread causes the object's
     * {@code run} method to be called in that separately executing
     * thread.
     * <p>
     * The general contract of the method {@code run} is that it may
     * take any action whatsoever.
     *
     * @see Thread#run()
     */
    @Override
    public void run() {
        worldManager.getWorlds().forEach(worldManager::setWorldSeason);
    }
}
